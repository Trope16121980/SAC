    public void GuardarNuevoId() {
        try {
            String line;
            BufferedReader br = null;
            br = new BufferedReader(new FileReader(fichero));
            while ((line = br.readLine()) != null) {
                String[] busqueda = line.split(";");
                if (txtId.getText().equals(busqueda[8])) {
                    JOptionPane.showMessageDialog(null, "\nEsta id ya existe.");
                    br.close();
                    return;
                } else if (txtEmpresa.getText().equals("")
                        || txtNombre.getText().equals("")
                        || txtApellido.getText().equals("")
                        || txtMaterial.getText().equals("")
                        || txtCantidad.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "\nFaltan campos por rellenar.");
                    br.close();
                    return;
                }
            }
            br.close();
            GuardarEntradaNueva();
            LimpiarCasillas();
            MostrarTabla();
            datos();
            DefaultTableModel model = (DefaultTableModel) this.tablaAlmacen.getModel();
            model.setRowCount(0);
            model.setRowCount(0);
            MostrarTabla();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, date.fecha() + "\nError: " + e);
        }

    }

