    public void GuardarModificaciones() {
        int i = 0;
        try {
            String line;
            String empresa = txtEmpresa.getText();
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String dni = txtDni.getText();
            String fecha = txtFechaAlta.getText();
            String id = txtId.getText();
            BufferedReader br = null;
            BufferedWriter bw = null;
            br = new BufferedReader(new FileReader(fichero));
            bw = new BufferedWriter(new FileWriter(fichero_temporal));

            while ((line = br.readLine()) != null) {
                String[] busqueda = line.split(";");
                if (busqueda[5].equals(id)) {
                    i++;
                    busqueda[0] = (busqueda[0].replace(busqueda[0], empresa));
                    busqueda[1] = (busqueda[1].replace(busqueda[1], nombre));
                    busqueda[2] = (busqueda[2].replace(busqueda[2], apellido));
                    busqueda[3] = (busqueda[3].replace(busqueda[3], dni));
                    busqueda[4] = (busqueda[4].replace(busqueda[4], fecha));
                    busqueda[5] = (busqueda[5].replace(busqueda[5], id));
                }
                bw.write(busqueda[0] + ';' + busqueda[1] + ';' + busqueda[2] + ';' + busqueda[3] + ';' + busqueda[4] + ';'
                        + busqueda[5] + "\n");
            }
            bw.flush();
            br.close();
            bw.close();
            if (i == 0) {
                JOptionPane.showMessageDialog(null, "\nNo se encotraron datos.");
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, date.fecha() + "\nError: " + e);
        }
        fichero.delete();
        fichero_temporal.renameTo(fichero);
    }

    public void GuardarNuevoId() {
        try {
            String line;
            BufferedReader br = null;
            br = new BufferedReader(new FileReader(fichero));
            while ((line = br.readLine()) != null) {
                String[] busqueda = line.split(";");
                if (txtId.getText().equals(busqueda[5])) {
                    JOptionPane.showMessageDialog(null, "\nEste id ya existe.");
                    br.close();
                    return;
                } else if (txtDni.getText().equals(busqueda[3])) {
                    JOptionPane.showMessageDialog(null, "\nEste dni ya existe.");
                    br.close();
                    return;
                } else if (txtEmpresa.getText().equals("")
                        || txtNombre.getText().equals("")
                        || txtApellido.getText().equals("")
                        || txtDni.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "\nFaltan campos por rellenar.");
                    br.close();
                    return;
                }
            }
            br.close();
            GuardarEntradaNueva();
            LimpiarCasillas();
            ListaBd();
            datos();
            DefaultTableModel model = (DefaultTableModel) this.TablaListaBdSinTarjeta.getModel();
            model.setRowCount(0);
            model.setRowCount(0);
            ListaBd();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, date.fecha() + "\nError: " + e);
        }

    }

