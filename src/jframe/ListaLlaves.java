    public void GuardarNuevoId() {
        try {
            String line;
            BufferedReader br = null;
            br = new BufferedReader(new FileReader(fichero));
            while ((line = br.readLine()) != null) {
                String[] busqueda = line.split(";");
                if (txtId.getText().equals(busqueda[2])
                        || txtNombreLlave.getText().equals(busqueda[0])
                        || txtNumLlave.getText().equals(busqueda[1])) {
                    JOptionPane.showMessageDialog(null, "\nEsta llave ya existe.");
                    br.close();
                    return;
                } else if (txtNombreLlave.getText().equals("")
                        || txtNumLlave.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "\nFaltan campos por rellenar.");
                    br.close();
                    return;
                }
            }
            br.close();
            GuardarEntradaNueva();
            LimpiarCasillas();
            ListaBd();
            datos();
            DefaultTableModel model = (DefaultTableModel) this.TablaListaLlaves.getModel();
            model.setRowCount(0);
            model.setRowCount(0);
            ListaBd();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, date.fecha() + "\nError: " + e);
        }

    }

