    public void MostrarTabla() {
        String line;
        int i = 0;
        BufferedReader br = null;
        DefaultTableModel model = (DefaultTableModel) TablaEntradaSalidaVisitas.getModel();
        TableRowSorter<TableModel> elQueOrdena = new TableRowSorter<TableModel>(model);
        model.setRowCount(0);
        model.setRowCount(0);
        try {
            br = new BufferedReader(new FileReader(fichero));
            while ((line = br.readLine()) != null) {
                String[] tabla = line.split(";");
                if (tabla[2].equalsIgnoreCase("DENTRO")
                        || tabla[1].equals(date.fecha()) && tabla[2].equalsIgnoreCase("FUERA")) {

                    TablaEntradaSalidaVisitas.setRowSorter(elQueOrdena);
                    Vector row = new Vector();
                    row.add(tabla[20]);
                    row.add(tabla[2]);
                    row.add(tabla[1]);
                    row.add(tabla[3]);
                    row.add(tabla[4]);
                    row.add(tabla[5]);
                    row.add(tabla[10]);
                    row.add(tabla[11]);
                    model.addRow(row);
                }
            }
            br.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "\nError: " + e);
        }
    }

