    public void BuscarDatos() {
        String line;
        int d = 0;
        try {
            BufferedReader brbd = null;
            brbd = new BufferedReader(new FileReader(bdSin));

            while ((line = brbd.readLine()) != null) {
                String[] cod_tar = line.split(";");
                if ((cod_tar[1].equalsIgnoreCase(txtNombre.getText()) && cod_tar[2].equalsIgnoreCase(txtApellido.getText()))) {
                    d++;
                    String empresa = cod_tar[0];
                    txtEmpresa.setText(empresa);
                    String dni = cod_tar[3];
                    txtDni.setText(dni);
                } else if (cod_tar[3].equalsIgnoreCase(txtDni.getText())) {
                    d++;
                    String empresa = cod_tar[0];
                    txtEmpresa.setText(empresa);
                    String nombre = cod_tar[1];
                    txtNombre.setText(nombre);
                    String apellido = cod_tar[2];
                    txtApellido.setText(apellido);
                }
            }
            brbd.close();
            if (d == 0) {
                JOptionPane.showMessageDialog(null, "\nNO SE ENCONTRARON DATOS PARA\nREALIZAR LA ENTRADA");
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "\nError: " + e);
        }
    }

